                            <h3>How to activate IVR on JustCall?</h3>
                            <p>With JustCall you can activate your IVR systems to create an interactive menu for your customers. With JustCall’s IVR you can set up options based on digit-based inputs from your customers and direct calls to any of your concerned departments. Here are a few simple steps you can follow:</p>
                              <ul>
                                  <li>Login to your JustCall account.</li>
                                  <li>Go to the sidebar and click on <strong>“Phone Numbers”.</strong></li>
                                  <li>Select a phone number in your catalog in which you want to set up your IVR.</li>
                                  <li>In the new menu, click on “IVR”.</li>
                                  <li>Enable IVR by clicking on the “Activate switch“.</li>
                                  <li>Create Actions on specific digits or your IVR module. By doing this you can create a menu using each digit on the num-pad.</li>
                                  <li>Add the required text for each of the action to be used as an audio message during the call.</li>
                                  <li>Set the required actions by Forwarding calls to specific departments or teams. Set up your own phone number if you wish to.</li>
                                  <li>You can also create an option to forward or dismiss any “Unanswered call”. For example: We’ve set an Unanswered call to be directed to the Voicemail.</li>
                                  <li>In the Text entry, you only need to add the message you want to convey to the caller. The digit will automatically correspond to the text you’ve added.For example: If you add “Contact the customer care center” in the text for Digit 1, the audio message that the caller will hear will be “Press 1 to contact the customer care center”</li>
                                  <li>Click on “Add option” and add more options for your caller by specifying the actions on different digits</li>
                                  <li>Once you’ve set up the IVR menu, click on the “Save Changes” button.</li>
                              </ul>

                                <p>With JustCall’s IVR feature, you can easily manage your callers by providing them an automatic response system where you can add actions on different digits on the num-pad. Provide an intelligent calling solution to your customers and clients with JustCall.</p>
